export function generateUsageStats(days: number): UsageStat[] {
  return Array.from({ length: days }, (_, i) => {
    const date = new Date();
    date.setDate(date.getDate() - (days - 1 - i));
    return {
      date: date.toISOString().slice(0, 10),
      inputTokens: Math.floor(Math.random() * 500000 + 100000),
      outputTokens: Math.floor(Math.random() * 200000 + 50000),
      requestCount: Math.floor(Math.random() * 1000 + 200),
      estimatedCost: parseFloat((Math.random() * 20 + 2).toFixed(2)),
    };
  });
}
